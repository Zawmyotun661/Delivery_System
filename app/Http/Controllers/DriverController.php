     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'address' => 'required',
            'phone' => 'required',
        ]);
        Driver::create([
            'name' => $request->name,
            'address'=>$request->address,
            'phone'=>$request->phone,
        ]);
        return redirect('drivers');
    }

